def plural(word, count=2, exception=None):
    if count == 1 or count == -1:
        return word

    if exception is not None:
        return exception

    lword = word.lower()
    last_two = lword[-2:]
    last_one = last_two[1]
    next_to_last = last_two[0]

    if word.isupper():
        s = "S"
        es = "ES"
        ies = "IES"
        ses = "SES"
        ves = "VES"
        zes = "ZES"
    else:
        s = "s"
        es = "es"
        ies = "ies"
        ses = "ses"
        ves = "ves"
        zes = "zes"

    #
    # RULE: Exceptions
    #
    if lword in _EXCEPTION:
        if word.isupper():
            return _EXCEPTION[lword].upper()
        if word[0].isupper():
            return _EXCEPTION[lword].capitalize()
        return _EXCEPTION[lword]

    #
    # RULE: Words that end in Y
    #
    if last_one == "y" \
    and next_to_last in _VOWELS:
        return word + s

    if last_one == "y":
        return word[:-1] + ies

    #
    # RULE: Words that end in CH, SH, SS, X
    #
    if last_two == "ch" \
    or last_two == "sh" \
    or last_two == "ss" \
    or last_one == "x":
        return word + es

    #
    # RULE: Words that end in IS
    #
    if last_two == "is":
        return word[:-2] + es

    #
    # RULE: Words that end in S, Z
    #
    if last_one == "s":
        return word + ses

    if last_one == "z":
        return word + zes

    #
    # RULE: Words that end in "O"
    #
    if last_one == "o" \
    and next_to_last not in _VOWELS:
        return word + es

    if last_one == "o":
        return word + s

    #
    # RULE: Words that end in F, FE, FF
    #
    if last_two == "ff":
        return word + s

    if last_two == "fe":
        return word[:-2] + ves

    if last_one == "f":
        return word[:-1] + ves

    # US->I ?
    # ON->A ?
    # UM->A ?
    # IX->XES ?
    # EX->CES ?

    #
    # RULE: Add an S
    #
    return word + s
